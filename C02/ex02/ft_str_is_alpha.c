#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int k;

	k = 0;
	while (str[k])
	{
		if (!(((str[k] >= 'A') && (str[k] <= 'Z')) ||
					((str[k] >= 'a') && (str[k] <= 'z'))))
		{
			return (0);
		}
		k++;
	}
	return (1);
}
